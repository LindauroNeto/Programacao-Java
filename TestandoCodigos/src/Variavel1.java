public class Variavel1 {

    public static void main(String[] args) {
        String nome = "Lindauro";
        System.out.println(nome);
        System.out.println("Olá " + nome + ", tudo bem com você?");

        int idade = 19;
        System.out.println(idade);
        System.out.println("Você tem " + idade + " anos.");

        float altura = 1.92f;
        System.out.println(altura);
        System.out.println("Você tem " + altura + "m.");

        char PrimLetra = 'L';
        System.out.println(PrimLetra);
        System.out.println("A primeira letra do seu nome é: " + PrimLetra);

        boolean boleano = true;
        System.out.println(boleano);
        System.out.println("O céu é azul = " + boleano);
    }
}